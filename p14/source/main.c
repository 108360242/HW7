#include<stdio.h>
#include<stdlib.h>

int main(void)
{
	FILE *fptr;
	char ch;
	int Count = 0;

	fptr = fopen("welcome.txt", "r");
	if (fptr!=NULL)
	{
		while ((ch = getc(fptr)) != EOF)
		{
			printf("%c", ch);
			Count++;
		}
		fclose(fptr);
		printf("\n總共有%d個字元\n", Count);
	}
	else
	{
		printf("檔案開啟失敗!!\n");
	}
	system("pause");
	return 0;
}